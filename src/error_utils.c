#include "pipex.h"

int	ft_dup(t_pip *s, int fd)
{
	int	f;

	f = dup(fd);
	if (f == -1)
	{
		close_fd(s->fdi, s->fdo, s->pipe[0], s->pipe[1]);
		err("Err dup", 1, 1);
	}
	return f;
}
